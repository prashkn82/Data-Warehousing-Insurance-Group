/* ============================================================================
   ####  USER-MANAGED COLD BACKUP  ####
   Insurance Data Warehouse — Oracle 9i
   Schedule : Performed weekly, with the database SHUT DOWN
   ============================================================================ */


/* ----------------------------------------------------------------------------
   ####  1. IDENTIFY ALL DATAFILES TO BACK UP  ####
   ---------------------------------------------------------------------------- */
SELECT file_name
FROM   dba_data_files;


/* ----------------------------------------------------------------------------
   ####  2. IDENTIFY CONTROL FILES  ####
   ---------------------------------------------------------------------------- */
SELECT name
FROM   v$controlfile;


/* ----------------------------------------------------------------------------
   ####  3. IDENTIFY REDO LOG FILES  ####
   ---------------------------------------------------------------------------- */
SELECT member
FROM   v$logfile;


/* ----------------------------------------------------------------------------
   ####  4. SHUT DOWN THE DATABASE CLEANLY  ####
   Run in SQL*Plus before copying any files
   ---------------------------------------------------------------------------- */
SHUTDOWN IMMEDIATE;


/* ----------------------------------------------------------------------------
   ####  5. COPY FILES AT THE OS LEVEL  ####
   ---------------------------------------------------------------------------- */
-- Example OS command (Windows):
-- xcopy C:\oracle\oradata\INSU_DW\*.* C:\backup\cold\ /Y

-- Example OS command (Linux/Unix):
-- cp /oracle/oradata/INSU_DW/*.dbf /backup/cold/


/* ----------------------------------------------------------------------------
   ####  6. RESTART THE DATABASE  ####
   ---------------------------------------------------------------------------- */
STARTUP;


/* ----------------------------------------------------------------------------
   ####  7. VERIFY DATABASE IS OPEN AFTER RESTART  ####
   ---------------------------------------------------------------------------- */
SELECT status
FROM   v$instance;


/* ============================================================================
   ####  END OF SCRIPT  ####
   ============================================================================ */
