   ---------------------------------------------------------------------------- */
-- BACKUP ARCHIVELOG ALL;


/* ----------------------------------------------------------------------------
   ####  5. LIST EXISTING BACKUPS  ####
   ---------------------------------------------------------------------------- */
-- LIST BACKUP SUMMARY;


/* ----------------------------------------------------------------------------
   ####  6. VALIDATE BACKUP RESTORABILITY  ####
   Validation only — does not perform an actual restore
   ---------------------------------------------------------------------------- */
-- RESTORE DATABASE VALIDATE;


/* ----------------------------------------------------------------------------
   ####  7. CHECK ARCHIVE LOG DESTINATION & USAGE  ####
   Run from SQL*Plus
   ---------------------------------------------------------------------------- */
SELECT destination,
       status
FROM   v$archive_dest
WHERE  status != 'INACTIVE';


/* ----------------------------------------------------------------------------
   ####  8. CHECK LAST SUCCESSFUL BACKUP DATE  ####
   ---------------------------------------------------------------------------- */
SELECT input_type,
       status,
       start_time,
       end_time
FROM   v$rman_backup_job_details
ORDER  BY start_time DESC;


